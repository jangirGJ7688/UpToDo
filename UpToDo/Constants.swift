struct Fonts {
    static func latoRegularFont(_ size: CGFloat) -> Font {
        return Font.custom("Lato-Regular", size: size)
    }
    static func latoBoldFont(_ size: CGFloat) -> Font {
        return Font.custom("Lato-Bold", size: size)
    }
}
